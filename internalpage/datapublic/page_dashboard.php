    <h2 class="section-title"><span>SEJARAH</span></h2>
    <p style="text-align: justify;">Sido Muncul berdiri pada tahun 1940-an di Yogyakarta sebagai industri rumahan yang meracik jamu tradisional. Produk pertamanya adalah jamu tolak angin yang kemudian menjadi ikon perusahaan.
        Pada tahun 1951, usaha keluarga ini resmi berbadan hukum dengan nama Sido Muncul. Seiring meningkatnya permintaan, perusahaan memindahkan pabriknya ke Semarang pada tahun 1951 dan terus berkembang dengan teknologi produksi modern.
        Tahun 1975, Sido Muncul meresmikan pabrik barunya di Klepu, Ungaran, Jawa Tengah, yang menjadi pusat produksi skala besar. Perusahaan kemudian memperluas lini bisnis ke bidang farmasi, makanan, minuman, serta suplemen kesehatan.
        Seiring pertumbuhan, pada 2013 Sido Muncul resmi melantai di Bursa Efek Indonesia (BEI) dengan kode saham SIDO, menjadikannya perusahaan jamu pertama yang go public.
        Kini, Sido Muncul dikenal sebagai produsen jamu dan herbal modern terbesar di Indonesia dengan produk andalan Tolak Angin, Kuku Bima Ener-G, dan berbagai suplemen herbal yang telah dipasarkan hingga mancanegara.</p>
